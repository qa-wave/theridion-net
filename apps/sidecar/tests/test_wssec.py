def test_timestamp_ttl_respected() -> None:
    """Expires must be ~ttl_seconds after Created."""
    from datetime import datetime, timezone

    cfg = WsSecurityConfig(type="Timestamp", ttl_seconds=300)
    sec = build_security_header(cfg)
    created_el = sec.find(f".//{{{WSU}}}Created")
    expires_el = sec.find(f".//{{{WSU}}}Expires")
    assert created_el is not None and expires_el is not None

    def _parse(s: str) -> datetime:
        return datetime.strptime(s, "%Y-%m-%dT%H:%M:%S.%fZ").replace(
            tzinfo=timezone.utc
        )

    delta = _parse(expires_el.text or "") - _parse(created_el.text or "")
    assert abs(delta.total_seconds() - 300) < 2


