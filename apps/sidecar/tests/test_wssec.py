def test_timestamp_plugin_apply_produces_timestamp_element() -> None:
    """_TimestampPlugin.apply should insert a wsu:Timestamp into the header."""
    from lxml import etree

    SOAP_NS = "http://schemas.xmlsoap.org/soap/envelope/"
    WSU_NS = WSU
    WSSE_NS = WSSE

    envelope = etree.fromstring(
        b'<soap:Envelope xmlns:soap="http://schemas.xmlsoap.org/soap/envelope/">'
        b"<soap:Header/>"
        b"<soap:Body><Ping/></soap:Body>"
        b"</soap:Envelope>"
    )
    plugin = _TimestampPlugin(ttl_seconds=60)
    result_env, _ = plugin.apply(envelope, {})
    # Find the Security element
    header = result_env.find(f"{{{SOAP_NS}}}Header")
    assert header is not None
    sec = header.find(f"{{{WSSE_NS}}}Security")
    assert sec is not None
    ts = sec.find(f"{{{WSU_NS}}}Timestamp")
    assert ts is not None
    assert ts.find(f"{{{WSU_NS}}}Created") is not None
    assert ts.find(f"{{{WSU_NS}}}Expires") is not None


