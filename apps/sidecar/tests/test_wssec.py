def test_username_token_digest_password_is_not_plaintext() -> None:
    cfg = WsSecurityConfig(
        type="UsernameToken",
        username="bob",
        password="pass123",
        password_type="PasswordDigest",
        add_nonce=True,
        add_created=True,
        add_timestamp=False,
    )
    sec = build_security_header(cfg)
    xml_str = ET.tostring(sec, encoding="unicode")
    assert "bob" in xml_str
    assert "PasswordDigest" in xml_str
    # Password MUST NOT appear in plaintext
    assert "pass123" not in xml_str
    # The value must look like a base64-encoded SHA-1 digest (28 chars)
    pw_el = sec.find(f".//{{{WSSE}}}Password")
    assert pw_el is not None
    digest_text = pw_el.text or ""
    decoded = base64.b64decode(digest_text)
    assert len(decoded) == 20, "SHA-1 digest must be 20 bytes"


def test_username_token_digest_hash_formula() -> None:
    """Verify the PasswordDigest formula: Base64(SHA-1(nonce+created+password))."""
    import hashlib

    cfg = WsSecurityConfig(
        type="UsernameToken",
        username="u",
        password="pw",
        password_type="PasswordDigest",
        add_nonce=True,
        add_created=True,
        add_timestamp=False,
    )
    sec = build_security_header(cfg)
    # Extract nonce, created, and digest from the element tree
    nonce_el = sec.find(f".//{{{WSSE}}}Nonce")
    created_el = sec.find(f".//{{{WSU}}}Created")
    password_el = sec.find(f".//{{{WSSE}}}Password")
    assert nonce_el is not None and created_el is not None and password_el is not None

    nonce_bytes = base64.b64decode(nonce_el.text or "")
    created_str = (created_el.text or "").encode("utf-8")
    raw = nonce_bytes + created_str + b"pw"
    expected = base64.b64encode(hashlib.sha1(raw).digest()).decode()  # noqa: S324
    assert password_el.text == expected


